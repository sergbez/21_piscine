int	ft_sqrt(int nb)
{
	int sqrt;

	if (nb > 0)
	{
		sqrt = 1;
		while ((sqrt * sqrt) <= nb)
			sqrt++;
		sqrt--;
		if ((sqrt * sqrt) == nb)
			return (sqrt);
	}
	return (0);
}